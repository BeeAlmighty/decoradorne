// Generates favicon + icon + OG/apple logo data from the real logo.
// Source: public/images/logo/logo.jpeg (white lockup floating in black portrait).
import sharp from 'sharp';
import { writeFileSync } from 'node:fs';

const SRC = 'public/images/logo/logo.jpeg';
const BRAND_BG = { r: 0x1a, g: 0x14, b: 0x10, alpha: 1 }; // #1A1410 espresso
const GOLD = '#C9A96E';
const CREAM = '#FAF7F4';

// 1) Isolate the full lockup by trimming the black surround.
const trimmed = await sharp(SRC).trim({ background: '#000000', threshold: 18 })
  .toBuffer({ resolveWithObject: true });
const { width: tw, height: th } = trimmed.info;

// white-on-black -> {color}-on-transparent, using luminance as alpha.
async function tintTransparent(buf, w, h, hex) {
  const alpha = await sharp(buf).greyscale().toColourspace('b-w').raw().toBuffer();
  return sharp({ create: { width: w, height: h, channels: 3, background: hex } })
    .joinChannel(alpha, { raw: { width: w, height: h, channels: 1 } })
    .png().toBuffer();
}

// 2) Tent mark = top ~48% of lockup, re-trimmed, strokes thickened (dilate).
const tentCrop = await sharp(trimmed.data)
  .extract({ left: 0, top: 0, width: tw, height: Math.round(th * 0.48) })
  .trim({ background: '#000000', threshold: 18 }).toBuffer({ resolveWithObject: true });
const SCALE = 3;
const tentAlpha = await sharp(tentCrop.data).greyscale()
  .resize({ width: tentCrop.info.width * SCALE, height: tentCrop.info.height * SCALE })
  .blur(SCALE * 1.1).linear(4.0, 0) // fatten strokes, clamps at 255
  .toColourspace('b-w').raw().toBuffer({ resolveWithObject: true });
const taw = tentAlpha.info.width, tah = tentAlpha.info.height;
const goldTent = await sharp({ create: { width: taw, height: tah, channels: 3, background: GOLD } })
  .joinChannel(tentAlpha.data, { raw: { width: taw, height: tah, channels: 1 } })
  .png().toBuffer();

// place a transparent logo centered on a brand-dark square
async function onDarkSquare(logoPng, size, padRatio) {
  const inner = Math.round(size * (1 - padRatio));
  const r = await sharp(logoPng).resize({ width: inner, height: inner, fit: 'inside' })
    .toBuffer({ resolveWithObject: true });
  return sharp({ create: { width: size, height: size, channels: 4, background: BRAND_BG } })
    .composite([{ input: r.data, gravity: 'center' }]);
}

// 3) Favicon assets (gold tent on dark)
await (await onDarkSquare(goldTent, 256, 0.14)).png().toFile('src/app/icon.png');
const ico48 = await (await onDarkSquare(goldTent, 48, 0.12)).png().toBuffer();

// minimal PNG-in-ICO container (1 image)
function pngToIco(png, px) {
  const header = Buffer.alloc(6);
  header.writeUInt16LE(0, 0); header.writeUInt16LE(1, 2); header.writeUInt16LE(1, 4);
  const entry = Buffer.alloc(16);
  entry.writeUInt8(px >= 256 ? 0 : px, 0);
  entry.writeUInt8(px >= 256 ? 0 : px, 1);
  entry.writeUInt8(0, 2); entry.writeUInt8(0, 3);
  entry.writeUInt16LE(1, 4); entry.writeUInt16LE(32, 6);
  entry.writeUInt32LE(png.length, 8); entry.writeUInt32LE(22, 12);
  return Buffer.concat([header, entry, png]);
}
writeFileSync('src/app/favicon.ico', pngToIco(ico48, 48));

// 4) Cream full lockup (transparent) for OG hero + apple icon -> base64 data URI
const LOCKUP_W = 760;
const lockupCream = await tintTransparent(trimmed.data, tw, th, CREAM);
const lockupResized = await sharp(lockupCream)
  .resize({ width: LOCKUP_W }).png({ compressionLevel: 9 }).toBuffer();
const dataUri = `data:image/png;base64,${lockupResized.toString('base64')}`;
const out =
`// AUTO-GENERATED by scripts/generate-brand-icons.mjs — do not edit by hand.
// Cream Decor Adorné lockup (transparent) cropped from the real logo,
// inlined as a data URI for use inside next/og ImageResponse (Satori).
export const LOGO_LOCKUP_CREAM = '${dataUri}';
export const LOGO_LOCKUP_RATIO = ${(tw / th).toFixed(4)}; // width / height
`;
writeFileSync('src/lib/logo-data.ts', out);

console.log('lockup:', tw, 'x', th, 'ratio', (tw/th).toFixed(3));
console.log('icon.png 256, favicon.ico 48, logo-data.ts (', (dataUri.length/1024).toFixed(0), 'KB base64 )');
